#!/usr/bin/env node
// generate-pom-index.mjs — build tests/playwright/support/pom/POM-INDEX.md from the page objects.
//
// The index is the fast-lookup used when building a spec from a source: grep the visible label
// (from @label JSDoc tags) -> exact page-object member -> call it. Regenerate after editing the POM.
//
// Usage: node scripts/generate-pom-index.mjs

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const here = path.dirname(fileURLToPath(import.meta.url));
const POM_DIR = path.resolve(here, '../tests/playwright/support/pom');
const OUT = path.join(POM_DIR, 'POM-INDEX.md');

const STOPWORDS = new Set([
  'async', 'await', 'return', 'if', 'for', 'while', 'switch', 'catch', 'constructor', 'function',
  'get', 'set', 'new', 'throw', 'const', 'let', 'var', 'else', 'do', 'try', 'super',
]);

function humanize(member) {
  return member
    .replace(/([a-z0-9])([A-Z])/g, '$1 $2')
    .replace(/^./, (c) => c.toUpperCase())
    .trim();
}

function parseFile(file) {
  const rows = [];
  const src = fs.readFileSync(file, 'utf8');
  const lines = src.split('\n');
  const base = path.basename(file);
  let currentClass = '';
  let pendingLabel = '';

  for (const raw of lines) {
    const line = raw.trim();

    const classMatch = line.match(/^export\s+class\s+([A-Za-z0-9_]+)/);
    if (classMatch) currentClass = classMatch[1];

    const labelMatch = line.match(/@label\s+(.+?)\s*\*\//);
    if (labelMatch) {
      pendingLabel = labelMatch[1].trim();
      continue;
    }

    // getter/property locator: readonly name = ...   OR   name(): Locator / async name(
    // Skip arrow callbacks (they contain =>) so we only pick up real class members.
    const propMatch = line.match(/^readonly\s+([A-Za-z0-9_]+)\s*[:=]/);
    // A method DEFINITION line ends with `{`, has a `): ReturnType`, or opens a multi-line
    // signature ending in `(`. This excludes call statements like `expect(input, '...')`.
    const looksLikeDefinition = /\{\s*$/.test(line) || /\)\s*:/.test(line) || /\(\s*$/.test(line);
    const isPrivate = /^(?:private|protected)\s/.test(line); // spec authors can't call these
    const methodMatch = !line.includes('=>') && looksLikeDefinition && !isPrivate
      ? line.match(/^(?:public\s+|private\s+|protected\s+|static\s+|async\s+)*([A-Za-z0-9_]+)\s*\(/)
      : null;
    const member = propMatch?.[1] ?? methodMatch?.[1];

    if (member && currentClass && !STOPWORDS.has(member)) {
      const isLocatorish =
        /locator|button|field|input|link|tab|dialog|option|grid|cell|menu|toast|group/i.test(member) ||
        /Locator/.test(line) ||
        Boolean(pendingLabel);
      if (isLocatorish) {
        rows.push({
          label: pendingLabel || humanize(member),
          tagged: Boolean(pendingLabel),
          cls: currentClass,
          member,
          file: base,
        });
      }
    }
    pendingLabel = '';
  }
  return rows;
}

function main() {
  if (!fs.existsSync(POM_DIR)) {
    console.error(`POM dir not found: ${POM_DIR}`);
    process.exit(1);
  }
  const files = fs
    .readdirSync(POM_DIR)
    .filter((f) => f.endsWith('.ts') && !f.endsWith('.d.ts'))
    .map((f) => path.join(POM_DIR, f));

  const seen = new Set();
  const rows = files.flatMap(parseFile).filter((r) => {
    const key = `${r.cls}.${r.member}`;
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
  rows.sort((a, b) => a.label.localeCompare(b.label) || a.cls.localeCompare(b.cls));

  const header = [
    '# POM Index',
    '',
    '> Generated by `scripts/generate-pom-index.mjs` (run `npm run pom:index`). Do not edit by hand.',
    '>',
    '> Building a spec from a source? Find the visible label below → call that page-object member.',
    '> Specs must not declare inline locators (enforced by `npm run lint:pom`).',
    '',
    `Members: ${rows.length}  ·  @label-tagged: ${rows.filter((r) => r.tagged).length}`,
    '',
    '| Visible label | Page class | Member | File |',
    '| --- | --- | --- | --- |',
  ];
  const body = rows.map((r) => `| ${r.tagged ? `**${r.label}**` : r.label} | ${r.cls} | \`${r.member}\` | ${r.file} |`);
  fs.writeFileSync(OUT, `${header.concat(body).join('\n')}\n`);
  console.log(`Wrote ${OUT} (${rows.length} members, ${rows.filter((r) => r.tagged).length} @label-tagged).`);
}

main();
